document.addEventListener("DOMContentLoaded", () => {
  function RandomCard() {
    let but = document.querySelector("[data-js='but']");
    let img = document.querySelector("[data-js='img']");
    let title = document.querySelector("[data-js='name']");
    let desc = document.querySelector("[data-js='disc']");
    let auth = document.querySelector("[data-js='author']");
    but.addEventListener("pointerdown", () => {
      let card;
      if (Math.floor(Math.random() * 3) == 0) {
        card = cardDatabase.categories.major[Math.floor(Math.random() * 23)];
      } else {
        switch (Math.floor(Math.random() * 4)) {
          case 0:
            card =
              cardDatabase.categories.minor.cups[
                Math.floor(Math.random() * 14)
              ];
            break;
          case 1:
            card =
              cardDatabase.categories.minor.wands[
                Math.floor(Math.random() * 14)
              ];
            break;
          case 2:
            card =
              cardDatabase.categories.minor.swords[
                Math.floor(Math.random() * 14)
              ];
            break;
          case 4:
            card =
              cardDatabase.categories.minor.pentacles[
                Math.floor(Math.random() * 14)
              ];
            break;
        }
      }
      img.src = card.pathToImage;
      title.textContent = card.name;
      desc.textContent = card.description;
      auth.textContent = card.author;
    });
  }
  RandomCard();
});
